            <td><p class="body">Most of us take pride in creating and maintaining the landscape surrounding our homes.
                    Landscaping increases the aesthetic and curb appeal of our dwellings and enhances property value.
                    Florida&rsquo;s nursery and landscaping industry is big business, significantly affecting Florida&rsquo;s
                    economy by creating thousands of jobs and impacting labor income. In 2005, landscape sales topped
                    $5.25 billion and sales from retail garden centers were estimated at $6.97 billion. The practice of
                    planting the right plant in the right place can be consistent with promoting these economic
                    benefits.</p>
                <p class="body">The idea behind using the right plant in the right place is to choose landscaping plants
                    that fit best into existing site conditions, thus minimizing the use of fertilizers, pesticides,
                    irrigation and labor. When these commonly used &ldquo;backyard chemicals&rdquo; are eliminated, or
                    applied at minimal rates, and less water is used for irrigation (waterwise landscaping), the
                    likelihood of these chemicals reaching the Indian River Lagoon through stormwater runoff and
                    adversely affecting IRL water quality decreases.</p>
                <p class="body"> In fact, one of the biggest uses of water in Florida is for lawn and landscape
                    irrigation. Because of ever changing cycles in Florida&rsquo;s natural water supply, water
                    conservation through waterwise landscaping in conjunction with prudent use of fertilizers and
                    pesticides is something we should all strive to achieve.</p>
                <p class="body">Florida Friendly LandscapingTM, <a href="http://fyn.ifas.ufl.edu/">http://fyn.ifas.ufl.edu/</a>,
                    a University of Florida IFAS extension website, advocates nine principles to encourage and promote
                    environmentally sustainable landscaping practices. First on the list is <strong>RIGHT PLANT, RIGHT
                        PLACE.</strong></p></td>
