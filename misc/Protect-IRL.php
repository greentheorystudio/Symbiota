            <td><p class="body"> The majority of rainwater from impervious surfaces such as paved streets, parking lots,
                    sidewalks and rooftops as well as from saturated fields and slopes lacking vegetation, is collected
                    by storm drains, swales (shallow, grassy channels that collect stormwater from streets and
                    sidewalks) and retention ponds. This stormwater runoff often enters canals and tributaries that
                    drain directly into the Indian River Lagoon (Fig. 4). For example, it is estimated that 75 billion
                    gallons of stormwater runoff in Brevard County makes its way into the IRL annually. Carried with
                    this runoff are pollutants such as heavy metals, fertilizers, sediments, animal wastes, oil and
                    grease from roadways ,anti-freeze, pesticides, household chemicals, bacteria, and an array of
                    organic and inorganic material.</p>

                <p class="body">In the IRL, these materials threaten water column and bottom dwelling organisms, promote
                    excessive algal growth, and can smother seagrasses. This &ldquo;non-point&rdquo; pollution is the
                    primary source of pollution in the IRL today and poses the biggest threat to its water quality. A
                    common misconception hindering individual efforts to lessen the effects of this pollution source is
                    that many of us think that water entering storm drains is initially directed to wastewater
                    facilities for treatment. This is not the case.</p>
