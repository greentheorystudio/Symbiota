            <td><p class="body">Indiscriminate use of fertilizers results in deteriorating water quality in the Indian
                    River Lagoon as well as waterways everywhere. Fertilizers are composed primarily of nitrogen,
                    phosphorus and potassium which are the major components required for plant growth. These chemicals,
                    when improperly used, can have significant adverse effects on the environment.</p>

                <p class="body">Although fertilizers can temporarily make a lawn look greener, they are far too often
                    applied incorrectly. Instead of being incorporated into plant tissue, excess nutrients work their
                    way into the lagoon directly through stormwater runoff or indirectly by seeping through the soil
                    into groundwater. When in the lagoon, these nutrients stimulate blooms of micro-algae (i.e.,
                    phytoplankton) as well as the proliferation of other aquatic plants. Blooms of certain species of
                    phytoplankton can create what are known as harmful algal blooms, i.e., &quot;HABs&quot;. HABs can
                    produce potent toxins that can have negative health consequences for both humans as well as marine
                    organisms.</p>

                <p class="body">Eutrophication is a process in aquatic systems that describes the subsequent decay of
                    rapidly growing plant material consuming available oxygen, thus creating hypoxic conditions that
                    threaten other plants and animals in the ecosystem. Larval and juvenile stages of fish and
                    invertebrates can be particularly sensitive to eutrophication. This decaying organic material
                    eventually settles to the bottom and, along with fine particles of silt and clay, creates a low
                    oxygen (hypoxic) layer often called muck. Hypoxic conditions can affect biodiversity by causing the
                    demise of species normally found in an area while promoting the growth of opportunistic species that
                    thrive in low oxygen environments. Muck can easily be resuspended and further affect water quality
                    and clarity. Dense phytoplankton blooms (harmful or otherwise) can also physically decrease the
                    amount of light penetrating the water column and thus pose as an additional threat to healthy
                    seagrass growth.</p>
