            <td><p class="body"> We all love our pets and treat them like family members. If we do not pick up after our
                    pets, we are doing a disservice to them, ourselves, our neighbors and the environment. Neglected pet
                    waste poses a major bacterial pollution threat to the Indian River Lagoon when it enters local
                    waterways and works its way into the IRL via stormwater runoff.</p>
                <p class="body">The Humane Society of the United States estimates that 39% of all households own at
                    least one dog. Based on this national trend, it is estimated that in Brevard County alone, there are
                    approximately 104,832 dogs. Dogs produce about one pound of waste per day. This material contains
                    fecal coliform bacteria (one gram of dog waste contains about 23 million coliform bacteria)
                    including salmonella. This material may also contain viruses and parasites such as roundworms,
                    hookworms and tapeworms. Many of these organisms present a potential human health hazard and can
                    cause severe cramping and diarrhea, particularly in children and the elderly. These organisms can
                    linger in the soil for years if associated animal waste is not disposed of properly. In urban areas,
                    dog waste is also a potential source of food for mice and rats. Rodents can carry a host of harmful
                    bacteria and be a source of ticks and fleas.</p>
                <p class="body">Some people are reluctant to pick up after their pets for a variety of reasons:
                    inconvenience; unsightliness; odor, etc. Although more than 60 % of dog owners do pick up after
                    their four-legged friends, it is estimated that in the five county region surrounding the Indian
                    River Lagoon, every day, as much as 167,530 pounds (84 tons) of dog feces is left on the ground in
                    backyards, sidewalks and streets. In addition, some misinformed citizens will dispose of animal
                    waste directly into waterways and/or storm drains. Since most stormwater is not treated before
                    flowing into canals and local waterways, bacteria and other organisms in neglected or improperly
                    disposed of pet waste can directly threaten commercial and recreational fishing, shellfishing,
                    boating, and swimming in the Indian River Lagoon. Nutrients associated with pet waste can also cause
                    the proliferation of phytoplankton and macro-algae with additional, adverse effects on the IRL
                    ecosystem (see above).</p></td>
