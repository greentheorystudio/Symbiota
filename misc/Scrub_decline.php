                <p class="body">
                    Populations of the Florida scrub jay, the state's only endemic bird, have been in decline since the
                    1950s, principally due to loss of the mainland scrub communities which are its primary habitat. The
                    Robinson and Smith study (1996) examined the relationship between increases in human population in
                    Brevard County, Florida with respect to declines in both the number of breeding pairs of threatened
                    Florida scrub jays, and the number of acres available to scrub jays as habitat. Note that as human
                    population (yellow line) increased between 1940 and 1990, the number of acres of available scrub
                    habitat (blue line), and the number of breeding scrub jay pairs (pink line) decreased. Between 1940
                    and 1990, the human population of Brevard County increased by over 2700%.&nbsp; Scrub habitat was
                    reduced by approximately 70%, as scrub communities were converted into agricultural, commercial and
                    residential properties. Coinciding with this loss of habitat, the number of breeding pairs of scrub
                    jays declined by 87%. Following development of natural scrub communities, what remained of scrub jay
                    habitat was poor in quality, and highly fragmented. This trend, which has been mirrored statewide,
                    remains a threat; not only to the scrub jay, but also to the many other species that rely on scrub
                    habitats for their survival.
                </p>
