                    <td><p class="title">Introduction</p>
                        <p class="body">Filter-feeding oyster are important members of the Indian River Lagoon
                            because they reduce turbidity and improve water quality by removing
                            suspended organics, phytoplankton, and detritus (Peterson et al. 2003).
                            Since the 1940s, over 250 acres of oyster habitat has been lost in the
                            Indian River Lagoon (Wilson et al. 2005).  This is evident in aerial photos
                            dating as far back as 1943 (Grizzle et al. 2002).   The extent to which
                            natural as well as anthropogenic effects including human-induced salinity
                            fluctuations, muck removal projects, and biotoxin accumulation has impacted
                            the Indian River Lagoon oyster reefs is not well understood.   Reports such
                            as the Independent Scientific Review of the Indian River Lagoon - South
                            Project Implementation Report (Bartell et al.) commissioned by the U. S.
                            Army Corps of Engineers Jacksonville District, the Indian River Lagoon
                            Biotoxin and Aquatic Animal Health History and Background Report (Provancha
                            and Van den Ende 2006), and the Indian River Lagoon Comprehensive
                            Conservation and Management Plan Update 2008 suggest that heavy boating,
                            poor management of sewage discharge, agricultural runoff, freshwater
                            flooding from flood control canals, muck removal, and biotoxins are having
                            negative impacts on the plants and animals that live in the Indian River
                            Lagoon.</p>
                        <p class="title">Boating Impacts</p>
                        <p class="body">The most pronounced and documented changes to the oyster reefs are the dead
                            margins appearing in areas of high boat use (Grizzle et al. 2002).  The
                            dead margins consist primarily of piles of dead oyster shells that occur
                            when boat propellers drag along the bottom in shallow areas displacing
                            living and dead oyster shells.  Considerable damage is also caused by boat
                            wakes which eroding both shorelines and oyster reefs and stir up bottom
                            sediments causing the water to cloud (Grizzle et al. 2002).   Wave action
                            caused by boats results in sediment build-up on the oyster beds as the
                            disturbed sediment moves out of the water column interfering with filter
                            feeding and larval settlement.   Although dead margins are sometimes
                            considered beneficial because they may create a buffer back reef against
                            wave-motion, current research suggests that oyster reefs with dead margins
                            have reduced habitat complexity as well as reduced diversity of species
                            (Stiner and Walters 2008).</p>

                        <p class="title">Salinity Fluctuations</p>
                        <p class="body">Salinity fluctuations in the Indian River Lagoon are a major concern.  They
                            can be caused by the input of freshwater from sewage discharge,
                            agricultural runoff, and freshwater from flood control canals.  Algal
                            blooms occur when environmental conditions are optimal for growth.
                            Discharge of nutrients from agricultural, ranch and urban areas cause an
                            increase in nutrients in the sediments of the lakes of Upper St. John River
                            Basin (Bremmer et al. 1999).   Freshwater runoff that causes nutrient
                            enrichment in the estuarine waters can lead to blooms of some species but
                            the data is not clear that this is true for all species (Burkholder 1998).
                            Blooms of some species occur during El Nino events when the water
                            temperatures are increased (Burkholder 1998).   Periods of reduced
                            salinities that lead to oyster death see subsequent algal blooms.    Point
                            and non-point runoff in the Indian River Lagoon do have impacts but it is
                            unclear how much impact relative to the dominant flood-control influences
                            (WS Arnold, personal communication).</p>
                        <p class="body">A decrease in salinity to < 10&permil; appears to shorten the reproductive and
                            settlement season of the oyster <i>Crassostrea virginica</i> (Bartell et al.), a
                            major reef builder in the estuaries of the Indian River Lagoon.   The C-44
                            canal is a major drainage canal that connects Lake Okeechobee with the
                            South Fork of the St. Lucie estuary. The fresh water flows from this flood
                            canal appear to result in low salinities for several weeks at a time every
                            year potentially interfering with the reproductive season of <i>C. virginica</i>
                            (Bartell et al., Wilson et al. 2005).   Recent unpublished data show that
                            fresh water from flood control canals in the St. Lucie and Sebastian
                            estuaries combined with effects of the hurricanes of 2004 and 2005 almost
                            completely eliminated live oysters from these areas.  While live oysters
                            did return within about one year, the chronic and long-term release of
                            fresh water particularly into the St. Lucie system appears to have reduced
                            the areal coverage of oysters in that estuary (W. S. Arnold, personal
                            communication). Drainage of fresh water into the St. Lucie Estuary and its
                            tributaries has also recently been shown to cause a shift in species
                            composition (Wilson et al. 2005).  Human management that results in
                            salinities outside of the suitable ranges for survival and reproductive
                            success of reef building oysters will negatively impact the reefs (W. S.
                            Arnold, personal communication).  In addition to the low salinity effects,
                            large, poorly timed, and poorly located freshwater discharges into the
                            Indian River Lagoon has resulted in the accumulation of large amounts of
                            muck on the floor of the lagoon, clouding of the water column, and the
                            destruction of oyster and sea grass beds (Bartell et al.)</p>

                        <p class="title">Dredging and Muck Removal</p>

                        <p class="body">Dredging and sand replenishment is a process used to replenish sand on
                            eroded beaches in the estuaries.  Near shore hard bottom habitats can be
                            buried during this process or waters can become silty blocking light and
                            interfering with the filter feeding of oysters.  In areas where dredging or
                            burial of oyster reefs occur, there is a significant of juvenile fish.
                            This disturbs ecologically and commercially important species.  Although
                            long term effects have not been measured, it is likely that these events
                            will result in permanent displacement of fish populations.  Natural
                            recovery from wave action is unlikely in areas where oyster reefs are
                            buried (Lindeman and Snyder 1998). <br><br>
                            Muck removal, primarily by dredging, is occurring in areas where the
                            accumulation of sediment in the Indian River Lagoon has resulted in the
                            destruction of seagrass beds and oyster reef habitats.   The purpose of
                            these projects is to create new space for oyster recruitment by removing
                            dead habitat.  However, while appearing beneficial, several problems have
                            arose including clouding of the water, algal blooms that quickly take over,
                            and the re-accumulation of sediment from other anthropogenic sources.  A
                            potential solution for these new problems is to set sediment traps in
                            affected areas (Bartell et al.).</p>

                        <p class="title">Biotoxins</p>

                        <p class="body">Biotoxins are an important health threat to humans consuming shellfish
                            from the Indian River Lagoon.   In addition, there are several reported
                            cases of marine mammals and fish becoming ill and/or dying after coming
                            in contact with their microscopic source of biotoxins (Burkholder 1998,
                            Steidinger et al. 1998).   The impact of biotoxins from harmful algal
                            blooms on the health of oysters and other invertebrate species is less
                            well documented.  Harmful algal blooms usually refer to expansive
                            growths of cytotoxic bluegreen algae and phytoplankton.  The Indian
                            River Lagoon is reported to host 21 potentially toxic microalgal
                            species (Provancha and Van den End 2006).</p>
                        <p class="body">The most commonly
                            encountered neurotoxins are saxitoxin, tetrodotoxin, and domoic acid.
                            In the Indian River Lagoon, saxitoxin is produced by <i>Pyrodinium</i> var.
                            <i>bahamense</i>, Alexandrium, and <i>Gymnodinium</i> and causes paralytic shellfish
                            poisoning.  In 1996, a bloom of <i>G. pulchellum</i> was reported to cause
                            fish (species: <i>Centropomus undecimalis</i>, <i>Mugil cephalus</i>, <i>Arius felis</i>,
                            <i>Sciaenops ocellatus</i>, <i>Archosargus probatocephalus</i>, and <i>Pogonias cromis</i>)
                            and invertebrate kills (species: <i>Callinectes sapidus</i>, and <i>Panaeus</i> spp.)
                            in the Indian River Lagoon (Steidinger et al. 1998).   Tetrodotoxin is
                            found in puffer fish and causes puffer fish poisoning.  There is
                            evidence suggesting that tetrodotoxin is produced by one or more marine
                            bacteria. Domoic acid is produced by Psedo-nitzschia and causes amnesic
                            shellfish poisoning.  The dinoflagellates or diatoms that produce many
                            of the biotoxins are always present and are not usually toxic except in
                            times when large blooms of these micro-algae occur.  During a bloom
                            filter feeding invertebrates such as clams, mussels, and oysters ingest
                            the microalgae and accumulate the biotoxins in their tissues which are
                            then passed on to higher level consumers (e.g. carnivorous gastropods
                            and crustaceans) that will also accumulate the biotoxins in their flesh
                            (Burkholder 1998, Provancha and Van den End 2006).  Some bivalves
                            exhibit symptoms of narcosis and altered feeding behavior in the
                            presence of <i>Gymnodium</i> and <i>Alexandrium</i> spp.  (Burkholder 1998).</p>
