                        <p class="body">Mangrove communities, occurring along the fringes of intertidal regions
                            throughout most of the IRL, stabilize shorelines and also provide habitat and nursery area
                            for IRL's many ecologically and recreationally important finfish and invertebrates. These
                            mangrove communities have adapted to occupy and maintain their position along the fringes of
                            the lagoon by accreting sediment at a rate in tune with sea level rise when it was occurring
                            at a relatively slow pace. Accelerated sea level rise could pose threat to these vital
                            communities by outpacing their ability to accumulate sediments at appropriate rates.</p>
                    </li>
                    <li>
                        <p class="body">Seagrass beds are indispensible to the overall health and water quality of the
                            IRL. They also provide sediment stabilization and complex habitat. In addition, seagrasses
                            oxygenate the water column, provide substratum for epiphytes and are a food source utilized
                            by manatees, urchins, conchs, some fish and sea turtles. Deeper waters associated with
                            accelerated sea level rise could diminish sunlight levels and adversely impact the
                            photosynthetic capacities of seagrasses leading to substantial decreases in seagrass
                            acreage.
                        </p></li>

                    <li>
                        <p class="body">Larval stages of some estuarine invertebrate organisms have a narrow range of
                            salinity tolerance. Increased salinity in the lagoon accompanying sea level rise could
                            negatively impact their life histories with consequences affecting their abundance and
                            diversity.</p>

                    <li>
                        <p class="body">Threats to biodiversity: Sea level rise and warmer water temperatures could
                            decrease the number of temperate species that co-occur in the IRL along side the more
                            sub-tropical species; Invasive and other opportunistic organisms could more easily establish
                            themselves under stressful conditions resulting from sea level rise, displacing native flora
                            and fauna.</p></li>

                    <li>
                        <p class="body">Other impacts of sea level rise: Low-lying upland wetlands and maritime hammocks
                            would also be at risk of drowning and salt water inundation. In many instances, these
                            habitats would have nowhere to retreat because of development along much of the IRL's
                            shoreline. Loss of mud flats and salt marshes (particularly in northern sections of the
                            lagoon) utilized by fish, bird and invertebrate communities is possible. The storm-buffering
                            capacity of barrier islands/dune plant communities along the IRL would also be compromised.
                        </p></li>
