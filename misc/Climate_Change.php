            <td><p class="title">Mapping the IRL Relative to Sea Level Rise:</p>

                <p class="body">There are two available reports focusing on the effects of sea level rise relative to
                    shorelines of the Indian River Lagoon: 1) The Impacts of Sea Level Rise to the Indian River Lagoon
                    Estuary, (Florida): An Application of Ecological and Economic Models. Final Report March 2011; and
                    2) The Likelihood of Shore Protection Along the Atlantic Coast of the United States. Volume 2: New
                    England and the Southeast.</p>
                <p class="body">The first report - The Impacts of Sea Level Rise to the Indian River Lagoon Estuary,
                    (Florida): An Application of Ecological and Economic Models. Final Report March 2011 - was prepared
                    for the EPA and Indian River Lagoon National Estuary Program (IRLNEP) to calculate habitat change
                    over time resulting from user-defined increases in sea level. The study uses a model - Sea Level
                    Affecting Marshes Model (SLAMM) and divides the IRL into 4 regions (northern, central, southern and
                    coastal) for model analyses. Using two scenarios (sea level rises of 0.4 and 1.2 meters) played out
                    over three time frames (present, 2050 and 2100) for both unprotected (allowing for wetland
                    migration) and protected, developed lands (blocking wetland migration), the report presents a
                    complete set of detailed maps that provide results at various geographical regions along the IRL in
                    a graphical format from North to South.</p>

                <p class="body">The second report, also prepared for the U.S. EPA, contains two sections relevant to sea
                    level rise and the IRL. These sections are available at the following websites:
                <ol>
                    <li><a href="http://risingsea.net/ERL/FL_EastCentral.html">http://risingsea.net/ERL/FL_EastCentral.html</a>
                        - focuses on Volusia and Brevard counties;
                    </li>
                    <li>
                        <a href="http://risingsea.net/ERL/FL_Treasure.html">http://risingsea.net/ERL/FL_Treasure.html</a>
                        - focuses on impacts of sea level rise in Indian River, St. Lucie, Martin as well as Palm Beach
                        counties.
                    </li>
                </ol>
                </p>
                <p class="body">These two studies are part of a nation-wide EPA sponsored program to promote planning
                    for and awareness of sea level rise in the context of global climate change among government
                    planners and citizens. The Southwest Florida Regional Planning Council (SWFRPC) was awarded a grant
                    from EPA to participate in the program by coordinating studies of sea level rise in Florida.
                    Consequently the East Central Florida Regional Planning Council (ECFRPC) and the Treasure Coast
                    Regional Planning Council (TCRPC) entered into a contract with SWFRPC to conduct studies within
                    respective coastal regions. Reports include color-coded maps that distinguish shore areas that are
                    likely to be protected from erosion, inundation, and flooding (areas where shore protection is
                    almost certain (brown), likely (red), or unlikely (blue)), and shores where natural shoreline
                    retreat likely will take place (i.e., areas where current environmental policies would preclude
                    shore protection and enable wetlands to migrate inland (light green)).</p>
