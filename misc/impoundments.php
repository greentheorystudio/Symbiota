                <tr>
                    <td><p class="body">Mosquito control impoundments are areas of salt marsh or mangrove forest that have been diked to allow control of water levels for purposes of mosquito control. Within the dikes, perimeter ditches are flooded artificially in order to control breeding and reproduction of salt marsh mosquitoes without the use of pesticides. Florida's mangroves and salt marshes have historically been problem areas in one important respect: they are preferred breeding habitat for salt marsh mosquitoes (Ochlerotatus taeniorrhynchus and O. sollicitans). These mosquitoes are an important nuisance species that affect the health of humans and domestic animals. Salt marsh mosquitoes do not reproduce by laying their eggs in standing water. Rather, they deposit eggs in the moist soils of high marsh above the water line in tidal wetlands (Provost 1976). Eggs will remain dormant, often for long periods of time, until water levels rise in response to rains or tides.</p>
                        <p class="body">Eggs hatch in the water and undergo several larval stages before developing into adult mosquitoes within 5 days. In the vicinity of the Indian River Lagoon, concerted efforts aimed at controlling salt marsh mosquitoes began in the mid-1920s (Platt et al. 1943) with construction of miles of hand-dug, parallel ditches. These efforts were not highly successful because of the heavy maintenance required to maintain the ditches, and because tidal effects in the ditched areas were generally of such low amplitude that little mosquito control was effected (Rey and Rutledge 2001).</p>
                        <p class="body">
                            In the 193's field experiments demonstrated that controlling water levels through impoundment would provide source reduction of mosquito populations by effectively controlling mosquito reproduction (Hull and Dove 1939). However, this experimental program was abandoned as water losses within the impoundment through seepage and evaporation became problematic. Attention then turned toward the use of pesticides such as DDT. However, by the 1950s, concerns over pesticide resistance in insects began to emerge, and the focus of mosquito control again shifted back to source reduction.</p>

                        <p class="body">
                            The first impoundments in Florida were built in Brevard County in 1954, with other counties soon following. By the 1970's, in excess of 40,000 acres of Florida's coastal wetlands had been impounded (Rey and Kain 1990). The majority of impoundments were constructed at the mean high water level and then flooded year round, closed off from adjacent estuarine waters. Some, however, were allowed to drain during the winter months, but were flooded again as mosquito breeding season approached.</p>

                        <p class="title">Negative effects of closed impoundments: </p>
                        <p class="body">Although impoundment for mosquito control is an effective method of controlling mosquito populations, there are often severe environmental impacts on impounded wetlands isolated from adjacent estuaries. Particularly important are issues of water quality degradation, isolation of important fishery species from critical nursery habitats, interruption of nutrient flow between wetlands and estuarine waters, creation of unnaturally high water levels, and hypersaline conditions that may develop in closed impoundments when evaporation of water occurs. Any negative changes in any of these physical parameters may lead to the elimination of vegetation from such areas (Rey and Rutledge 2001).
                        </p>

                        <p class="title">Water levels:</p>
                        <p class="body">Excessively high water levels brought on by overflooding impoundments eliminated some species from salt marsh and mangrove communities altogether. While only a thin film of water is enough to prevent oviposition by salt marsh mosquitoes; impoundments are typically flooded to depths of 15 - 50 cm above the surface to compensate for evaporation effects (Rey et al. 1991). In closed impoundments, this practice eliminated some species such as saltwort (Batis maritima), and glasswort (Salicornia bigelovii, and Salicornia virginica), and also impacted black mangroves due to their short pneumatophores not being able to withstand prolonged flooding (Rey and Rutledge 2001).</p>

                        <p class="title">Water quality:</p>
                        <p class="body">
                            Closed impoundments showed significant changes in both water quality and soil chemistry. In many areas, isolation of flooded impoundments resulted in decreased dissolved oxygen concentrations and increases in both nitrogen and sulfide concentrations in soils. Some impoundments flooded by use of artesian wells showed ecological turnovers from having communities composed of predominantly halophytic species, to communities characteristic of fresh water habitats. Other impoundments were subject to hypersaline conditions when estuarine waters were pumped in to flood them during warm summer months. Because these impoundments were closed to adjacent waters, lack of flushing and evaporation resulted in extremely high salinities, which caused local extinctions of some species (Rey and Rutlege 2001).</p>

                        <p class="title">Effects on fish and invertebrates:</p>
                        <p class="body">Fish species were greatly affected by closed impoundments, with numbers of some species being significantly reduced in species that utilized salt marsh or mangrove areas as nursery grounds (Harrington and Harrington 1961, Snelson 1976, Gilmore et al. 1982, Rey et al 1990). Tarpon (Megalops atlanticus), ladyfish (Elops saurus), common snook (Centropomus undecimalis), mullet (Mugil cephalus), and other species important to commercial and recreation fisheries were adversely impacted by closed impoundments. Marine invertebrates were also impacted by isolation of impounded wetlands, with biodiversity and species abundance changing dramatically in some areas. In some areas, the invertebrate community became more characteristic of freshwater wetlands than marine or estuarine wetlands (Brockmeyer et al. 1997).</p>

                        <p class="title">Nutrient flow:</p>
                        <p class="body">In closed impoundments, natural patterns of nutrient flow are interrupted between mangrove areas and adjacent waters. In unaltered systems, nutrients from mangrove leaf fall, which are decomposed into particulate and dissolved forms, are utilized in a variety of ways by many different organisms as mangroves are flushed by tides. In closed impoundments, however, nutrients are never flushed from mangrove areas because there is no connection to estuarine waters, and thus remain confined within impoundments.</p>

                        <p class="title">Improved Impoundment Strategies:</p>

                        <p class="body">An improved strategy for impoundments was experimented with as early as the mid-1960s. This involved seasonal flooding of impoundments during peak mosquito breeding season. For the remainder of the year, impoundments were opened via culverts penetrating the dike so that water levels within the impoundment could fluctuate naturally with tides. In 1974, seasonal impoundment was combined with active water management. This strategy of allowing for impoundments to be adequately flushed by tides not only controlled salt marsh mosquitoes, but also helped to retain black mangroves and other vegetation, and allowed the return of juvenile fishes to nursery areas unavailable to them in closed impoundments. This management strategy is currently referred to as Rotational Impoundment Management (RIM).</p>
                        <p class="body">
                            Under RIM, estuaries retain many of their natural functions, and their primary productivity can rival that of unaltered wetlands (Lahmann 1998, Rey et al. 1990b). Culverts remain open between the impoundment and the estuary from October to May to allow water exchange and use of impoundments by transient fish species and invertebrates. Then, during the summer months, culverts are closed and impoundments flooded to the minimum levels needed to prevent oviposition in salt marsh mosquitoes. Low areas of the surrounding dike, called spillways, insure that water levels do not exceed prescribed levels, thus preventing injury to vegetation. RIM has proven to be an effective strategy for controlling mosquitoes while minimizing serious environmental impacts to estuaries. Data from Rey et al. (1991) shows that RIM is currently the most commonly employed management strategy in 3 of the 5-counties adjacent to the Indian River Lagoon. St. Lucie County has 1,371 hectares (ha) (3386.4 acres) of wetlands under RIM, Brevard County has 1,037 ha (2561.4 acres), and Indian River County has 448 ha (1106.5 acres). </p>

